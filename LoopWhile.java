public class LoopWhile {
    public static void main(String[] args) {
        for(int i = 1; i <= 10; i++){
            System.out.println("result in for loop"+i);
        }
        
        int iwhile = 1;
        while (iwhile <= 10) {
            System.out.println("result in while loop"+iwhile);
            iwhile++ ;
        }
    }
}
